  /* --------------------------------------------------------------------------
   * Index builder.
   *   - Validate each club's eras tile [1990, 2026] with no gaps/overlaps.
   *   - Expose eraForYear() so the slot machine can land on ANY year.
   *   - Expose COMBOS (one per era) for any era-level UI / CPU sampling.
   * ------------------------------------------------------------------------*/
  var START_YEAR = 1990, END_YEAR = 2026;

  function eraForYear(clubIndex, year) {
    var eras = CLUBS[clubIndex].eras;
    for (var i = 0; i < eras.length; i++) {
      if (year >= eras[i].from && year <= eras[i].to) return eras[i];
    }
    // Clamp to nearest era if a year falls outside (shouldn't happen if tiled).
    if (year < eras[0].from) return eras[0];
    return eras[eras.length - 1];
  }

  function buildIndex(clubs) {
    var combos = [];
    clubs.forEach(function (c, ci) {
      c.eras.forEach(function (e, ei) {
        var counts = { GK: 0, DEF: 0, MID: 0, FWD: 0 };
        e.players.forEach(function (pl) { counts[pl.pos]++; });
        combos.push({
          clubIndex: ci, eraIndex: ei,
          club: c.club, short: c.short, color: c.color, country: c.country,
          label: e.label, from: e.from, to: e.to, counts: counts, players: e.players,
        });
      });
    });
    return combos;
  }

  // Sanity check coverage at load (logged in Node; harmless in browser).
  function validateCoverage(clubs) {
    var problems = [];
    clubs.forEach(function (c) {
      var sorted = c.eras.slice().sort(function (a, b) { return a.from - b.from; });
      if (sorted[0].from > START_YEAR) problems.push(c.club + " starts at " + sorted[0].from);
      if (sorted[sorted.length - 1].to < END_YEAR) problems.push(c.club + " ends at " + sorted[sorted.length - 1].to);
      for (var i = 1; i < sorted.length; i++) {
        if (sorted[i].from !== sorted[i - 1].to + 1) {
          problems.push(c.club + " gap/overlap " + sorted[i - 1].to + "->" + sorted[i].from);
        }
      }
    });
    return problems;
  }

